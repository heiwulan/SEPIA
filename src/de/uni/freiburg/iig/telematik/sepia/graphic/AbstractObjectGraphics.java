package de.uni.freiburg.iig.telematik.sepia.graphic;

/**
 * Abstract superclass of all object graphic classes.
 * 
 * @author Adrian Lange
 */
public abstract class AbstractObjectGraphics {

	/**
	 * Returns <code>true</code> if at least one of the object's graphic attributes has content.
	 */
	public abstract boolean hasContent();
}
